package hr.fer.zemris.java.fractals;

public class Pair {
    private Integer workers;
    private Integer tracks;

    public Pair(Integer workers, Integer tracks) {
        this.workers = workers;
        this.tracks = tracks;
    }

    public Integer getWorkers() {
        return workers;
    }

    public Integer getTracks() {
        return tracks;
    }
}
